//! @summary   タイトルシーンの更新処理
//!
//! @parameter [titleSceneObject] タイトルシーンで使用するオブジェクト
//!
//! @return    なし
//--------------------------------------------------------------------
void UpdateTitleScene(TitleSceneObject* titleSceneObject)
{
	// スペースキーが入力される
	if (IsButtonPressed(PAD_INPUT_10))
	{
		// プレイシーンへ遷移する
		ChangeScene(SCENE_PLAY);
	}
}



